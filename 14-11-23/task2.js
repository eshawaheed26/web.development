var day = "Sunday";
switch(day){
    case 'Monday':
    console.log('Do something good');
    break;
    case 'Tuesday':
    console.log('Do something bad');
    break;
    case 'Wednesday':
    console.log('Do something worst');
    break;
    case 'Thursday':
    console.log('Do something dumb');
    break;
    case 'Friday':
    console.log('Do something stupid');
    break;
    case 'Saturday':
    console.log('Do something creepy');
    break;
    case 'Sunday':
    console.log('Do something fun');
    break;
    default:
    console.log("There is no such day")
}