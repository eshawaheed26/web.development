// First use keyword of function=function, Name of Funtion, () - Input to Function
// For calling the function, you write the function name

function addTwoNums(){
    var a = 10;
    var b = 20;
    var c = a + b;
    console.log(c);
} addTwoNums();

// It can be done either way.
function addTwoNums(a,b){
    var c = a + b;
    console.log(c);
} addTwoNums(10,10);
